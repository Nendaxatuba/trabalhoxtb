
package br.com.model;

public class ItemLoca {
    private int id;
    private int locacao_id;
    private int carro_id;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the locacao_id
     */
    public int getLocacao_id() {
        return locacao_id;
    }

    /**
     * @param locacao_id the locacao_id to set
     */
    public void setLocacao_id(int locacao_id) {
        this.locacao_id = locacao_id;
    }

    /**
     * @return the carro_id
     */
    public int getCarro_id() {
        return carro_id;
    }

    /**
     * @param carro_id the carro_id to set
     */
    public void setCarro_id(int carro_id) {
        this.carro_id = carro_id;
    }
    
    
}
